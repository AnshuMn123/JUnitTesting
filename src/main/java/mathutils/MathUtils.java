package mathutils;

public class MathUtils {
    public int add(int a, int b){
        return a + b;
    }

    public int subtract(int a, int b){
        return a - b;
    }

    public int multiple(int a, int b){
        return a * b;
    }

    public int divide(int a, int b){
        return a / b;
    }

    public double circumferenceCircle(int x){
        return Math.PI*x*x;
    }
}
